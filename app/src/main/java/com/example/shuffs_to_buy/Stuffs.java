package com.example.shuffs_to_buy;



public class Stuffs {

    String name;
    String id;

    public Stuffs(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setAmount(String name) {
        this.name = name;
    }


}
